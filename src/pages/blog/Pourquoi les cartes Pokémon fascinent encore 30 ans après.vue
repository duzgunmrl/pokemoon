        <p>
          Au cœur de son esthétique se trouve une volonté de déconstruire la silhouette traditionnelle. Contrairement à la mode classique qui cherche souvent à valoriser la silhouette, Rick Owens préfère la distordre. Ses créations jouent avec des volumes exagérés, des proportions inattendues, et des tissus lourds qui semblent presque sculptés, comme des pièces d'art vivantes. Il transforme le corps humain en un canevas, où chaque pièce de vêtement devient une exploration de la forme et de l’espace.
        </p>
        <p>
